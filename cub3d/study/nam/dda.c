void	cal_dda(t_gra *a, t_pos *p)
{
	t_dda		dda;

	dda.cameraX = 2 * p->x / (double)w - 1;
	dda.rayDirX = p->dirX + p->planeX * dda.cameraX;
	dda.rayDirY = p->dirY + p->planeY * dda.cameraX;
	dda.mapX = (int)p->posX;
	dda.mapY = (int)p->posY;
	dda.deltaDistX = abs(1 / dda.rayDirX);
	dda.deltaDistY = abs(1 / dda.rayDirY);
	dda.hit = 0;
	cal_dda_2(a, p, &dda);
}

void	cal_dda_2(t_gra *a, t_pos *p, t_dda *dda)
{
	cal_dda_2_1(p, dda);
	while (dda->hit == 0)
		cal_dda_2_2(a, p, dda);
	cal_dda_2_3(a, p, dda);
}

void	cal_dda_2_1(t_pos *p, t_dda *dda)
{
	if (dda->rayDirX < 0 && (dda->stepX = -1))
		dda->sideDistX = (p->posX - dda->mapX) * dda->deltaDistX;
	else if ((dda->stepX = 1))
		dda->sideDistX = (dda->mapX + 1.0 - p->posX) * dda->deltaDistX;
	if (dda->rayDirY < 0 && (dda->stepY = -1))
		dda->sideDistY = (p->posY - dda->mapY) * dda->deltaDistY;
	else if ((dda->stepY = 1))
		dda->sideDistY = (dda->mapY + 1.0 - p->posY) * dda->deltaDistY;
}

void	cal_dda_2_2(t_gra *a, t_pos *p, t_dda *dda)
{
	if (dda->sideDistX < dda->sideDistY)
	{
		dda->sideDistX += dda->deltaDistX;
		dda->mapX += dda->stepX;
		dda->side = 0;
	}
	else
	{
		dda->sideDistY += dda->deltaDistY;
		dda->mapY += dda->stepY;
		dda->side = 1;
	}
	if (worldMap[dda->mapX][dda->mapY] > 0)
		dda->hit = 1;
}

void	cal_dda_2_3(t_gra *a, t_pos *p, t_dda *dda)
{
	if (dda->side == 0)
		dda->perpWallDist = (dda->mapX - p->posX + (1 - dda->stepX) / 2) / dda->rayDirX;
	else
		dda->perpWallDist = (dda->mapY - p->posY + (1 - dda->stepY) / 2) / dda->rayDirY;
	dda->lineHeight = (int)(h / dda->perpWallDist);
	dda->drawStart = -(dda->lineHeight) / 2 + h / 2;
	if (dda->drawStart < 0)
		dda->drawStart = 0;
	dda->drawEnd = dda->lineHeight / 2 + h / 2;
	if (dda->drawEnd >= h)
		dda->drawEnd = h - 1;
	dda->color = 0xFF0000;
	dda->tmp_y = dda->drawStart;
	while (dda->tmp_y++ < dda->drawEnd)
		mlx_pixel_put(a->mlx, a->win, p->x, dda->tmp_y, dda->color);
}
